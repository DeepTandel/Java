package String;

public class IndexOf {
    public static void main(String[] args) {
        String s = "Deep";
        int i = s.indexOf("e");
        int j = s.indexOf("p");
        System.out.println("Index : "+i);
        System.out.println("Index : "+j);
        System.out.println("Index : "+s.indexOf("D"));

    }
}
